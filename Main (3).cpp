#include <iostream> 
#include <cmath>  // Include cmath for math functions 
using  namespace  std ; 
int  main () { 
double  num =  16.0 ; 
double  base =  2.0 , exponent =  3.0 ; 
// Using sqrt() function to find the square root 
cout  <<  "Square root of "  << num <<  " is "  <<  sqrt (num) << 
endl ; 
// Using pow() function to calculate power 
cout  << base <<  " raised to the power "  << exponent  <<  " is " 
<<  pow (base, exponent) <<  endl ; 
return  0 ; 
}